function parseBold(markdown) {

// (\*\*|__) - グループ1: ** または __

// ([^\s][^*_]*[^\s]) - グループ2: 太字テキスト

// [^\s] - 最初の文字はスペースではない

// [^*_]* - * や _ 以外の文字が0個以上

// [^\s] - 最後の文字はスペースではない

// \1 - グループ1と同じ文字列（開始と終了が同じ記号）

  const regex = /(\*\*|__)([^\s][^*_]*[^\s])\1/g; // /gフラグ（グローバル）があるから複数回変換可能。
  // /gフラグなしだと、最初のひとつだけ変換される。
  
  return markdown.replace(regex, "<b>$2</b>");
}